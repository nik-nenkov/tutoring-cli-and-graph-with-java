    @Test
    void toPrintWeirdTable() {
        Table t = new Table();
        t.putCell(new Cell("E8", 1));
        t.putCell(new Cell("D1", 753436));
        t.putCell(new Cell("B4", 44355656));
        t.putCell(new Cell("C3", 3));
        assertEquals("\n" +
                "   ||  A  |         B  |  C  |       D  |  E  |\n" +
                "==========|============|=====|==========|=====|\n" +
                " 1 ||     |            |     |  753436  |     |\n" +
                "-----------------------------------------------\n" +
                " 2 ||     |            |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 3 ||     |            |  3  |          |     |\n" +
                "-----------------------------------------------\n" +
                " 4 ||     |  44355656  |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 5 ||     |            |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 6 ||     |            |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 7 ||     |            |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 8 ||     |            |     |          |  1  |\n" +
                "-----------------------------------------------", t.toPrint());
        Cell c = t.getCell("B4");
        c.setExpression(parse("44355656-1"));
        assertEquals("\n" +
                "   ||  A  |         B  |  C  |       D  |  E  |\n" +
                "==========|============|=====|==========|=====|\n" +
                " 1 ||     |            |     |  753436  |     |\n" +
                "-----------------------------------------------\n" +
                " 2 ||     |            |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 3 ||     |            |  3  |          |     |\n" +
                "-----------------------------------------------\n" +
                " 4 ||     |  44355655  |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 5 ||     |            |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 6 ||     |            |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 7 ||     |            |     |          |     |\n" +
                "-----------------------------------------------\n" +
                " 8 ||     |            |     |          |  1  |\n" +
                "-----------------------------------------------", t.toPrint());
    }

    @Test
    void tableGetCell() {
        Table t = new Table();
        t.putCell(new Cell("E8", 1));
        t.putCell(new Cell("D1", 753436));
        t.putCell(new Cell("B4", 44355656));
        t.putCell(new Cell("C3", 3));
        assertEquals("3", t.getCell("C3").toString());
    }

    @Test
    void putCellWhenNoPreviousRecordOnThisPosition() {
        Table t = new Table();
        t.setCell("A1", parse("(10-4)/2"));
        assertEquals("\n" +
                "   ||  A  |\n" +
                "==========|\n" +
                " 1 ||  3  |\n" +
                "-----------", t.toPrint());
    }
